# Example of Hierarchical Inheritance

# Define the base class
class BaseClass:
    pass  # This is the common base class from which other classes will inherit

# Define a derived class A that inherits from BaseClass
class A(BaseClass):
    pass  # Class A inherits all properties and methods from BaseClass (if any)

# Define another derived class B that inherits from BaseClass
class B(BaseClass):
    pass  # Class B also inherits all properties and methods from BaseClass

# Define a third derived class C that inherits from BaseClass
class C(BaseClass):
    pass  # Class C also inherits all properties and methods from BaseClass

    
