import numpy as np  # Importing the NumPy library for working with arrays

# Creating a 1D array of zeros with 5 elements
a = np.zeros(5)  # The array will contain 5 elements, all initialized to 0
print(a)  # Printing the 1D array of zeros
print()  # Printing an empty line for better readability

# Creating a 2D array of zeros with shape (3, 3)
b = np.zeros((3, 3))  # The array will have 3 rows and 3 columns, all elements set to 0
print(b)  # Printing the 2D array of zeros

# Printing the type of the variable 'a' to confirm it's a NumPy array
print(type(a))  # Output: <class 'numpy.ndarray'>
