# Importing the NumPy library, which is used for numerical and array operations
import numpy as np

# Creating a Python list with integer elements
my_list = [1, 2, 3, 4]

# Converting the Python list into a NumPy array
x = np.array(my_list)

# Printing the NumPy array to see the output
print(x)

# Printing the type of the variable 'x' to verify it is a NumPy array
print(type(x))
