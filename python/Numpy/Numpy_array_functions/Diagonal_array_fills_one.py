import numpy as np  # Importing the NumPy library for working with arrays

# Creating a 3x3 identity matrix using np.eye
diagonal1 = np.eye(3)  # np.eye(n) creates an identity matrix of size n x n
print(diagonal1)  # Printing the 3x3 identity matrix

print()  # Printing an empty line for better readability

# Creating a 4x4 identity matrix using np.eye
diagonal2 = np.eye(4, 4)  # np.eye(m, n) creates an identity matrix of size m x n
print(diagonal2)  # Printing the 4x4 identity matrix
