import numpy as np  # Importing the NumPy library for working with arrays

# Creating a 1D array of ones with 4 elements
a = np.ones(4)  # The array will contain 4 elements, all initialized to 1
print(a)  # Printing the 1D array of ones
print()  # Printing an empty line for better readability

# Creating a 2D array of ones with shape (3, 4)
b = np.ones((3, 4))  # The array will have 3 rows and 4 columns, all elements set to 1
print(b)  # Printing the 2D array of ones

# Printing the type of the variable 'a' to confirm it's a NumPy array
print(type(a))  # Output: <class 'numpy.ndarray'>
