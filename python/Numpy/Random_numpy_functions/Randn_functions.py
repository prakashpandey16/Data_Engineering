# Importing the numpy library for numerical operations
import numpy as np

# np.random.randn() generates random numbers from the standard normal distribution (mean = 0, standard deviation = 1).
# It creates an array of the specified shape, here generating 5 random numbers.
var = np.random.randn(5)

# Printing the 1D array of 5 random numbers from the standard normal distribution
print(var)
