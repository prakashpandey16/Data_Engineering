# Define a list with elements
my_list = [1, 2, 3, 4]

# Use the dir() function to get a list of attributes and methods of the list object
print(dir(my_list))  

# Why use dir():
# The dir() method is used to explore the capabilities of an object in Python.
# It provides a list of all attributes (variables) and methods (functions) that the object supports.
# This is particularly helpful for debugging, learning, or understanding what you can do with an object.
