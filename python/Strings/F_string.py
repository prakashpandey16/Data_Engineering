# Assigning values to variables
name = "Prakash"  # Name of the person
country = "India"  # Country of the person

# Using an f-string to print the message
print(f"My name is {name} and I am from {country}.")  # Outputs: My name is Prakash and I am from India.

# Assigning a floating-point value to price
price = 56.23434  # Price with more than 2 decimal places

# Correcting the f-string to format the price to 2 decimal places
print(f"Price is : {price:.2f}")  # Outputs: Price is : 56.23

# If you want to store an f-string with curly braces {{name}} and {{country}} as part of the string
print(f"My name is {{name}} and I am From {{country}}")  # Outputs: My name is {name} and I am From {country}



